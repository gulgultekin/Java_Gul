package day18_NestedLoop;

public class do_While_Loop {
    public static void main(String[] args) {

        int i =5;
        do{

            System.out.println("Hello world");

        }while(i==0); //false


        System.out.println("************************************");

        while(i==0){
            System.out.println("Hello world");

        }
    }
}
