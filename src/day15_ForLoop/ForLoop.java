package day15_ForLoop;

public class ForLoop {

    public static void main(String[] args) {

        for(int i=0; i<10; i++){

            System.out.println("Hello World "+i);
        }


        for(int a=1; a<=10; a+=2){
            System.out.println(a);

        }

        //square
        for (int b=1; b<=10; b++){
            System.out.println(b+" : "+ (b*b));
        }

    }
}
